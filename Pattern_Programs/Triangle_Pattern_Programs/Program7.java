package Triangle_Pattern_Programs;

public class Program7 {

	public static void main(String[] args) 
	{
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				for(int k=1;k<=i;k++)
				{
				System.out.print("*");
				}
			
			}
			System.out.println();
		}
		
		
	}

}
